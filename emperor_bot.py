    aspects.append("¬∑ –ì–∞—Ä–º–æ–Ω–∏—á–Ω—ã–π –∞—Å–ø–µ–∫—Ç –º–µ–∂–¥—É 2-–º –∏ 8-–º –¥–æ–º–∞–º–∏ ‚Äî –≤–æ–∑–º–æ–∂–Ω—ã –ø–æ—Å—Ç—É–ø–ª–µ–Ω–∏—è –∏–∑ –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω—ã—Ö –∏—Å—Ç–æ—á–Ω–∏–∫–æ–≤.")
    aspects.append("¬∑ –£–ø—Ä–∞–≤–∏—Ç–µ–ª—å 2-–≥–æ –¥–æ–º–∞ –≤ —Ö–æ—Ä–æ—à–µ–º –ø–æ–ª–æ–∂–µ–Ω–∏–∏ ‚Äî –æ—Å–Ω–æ–≤–Ω—ã–µ –¥–µ–Ω–µ–∂–Ω—ã–µ –∫–∞–Ω–∞–ª—ã —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∏—Ä—É—é—Ç —Å—Ç–∞–±–∏–ª—å–Ω–æ.")
