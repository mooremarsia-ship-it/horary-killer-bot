def _financial_horary_analysis(self, question, moon_sign, sun_sign):
    """–ì–õ–£–ë–û–ö–ò–ô —Ñ–∏–Ω–∞–Ω—Å–æ–≤—ã–π –∞–Ω–∞–ª–∏–∑ —Å –ø—Ä–æ–≥–Ω–æ–∑–∞–º–∏"""
    
    question_lower = question.lower()
    current_time = datetime.now(timezone.utc) + timedelta(hours=3)
    time_str = current_time.strftime('%H:%M, %d %B %Y')
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≤—Ä–µ–º–µ–Ω–Ω–æ–π –ø–µ—Ä–∏–æ–¥ –≤–æ–ø—Ä–æ—Å–∞
    time_period = "–±–ª–∏–∂–∞–π—à–∏–µ –¥–Ω–∏"
    if '–∑–∞–≤—Ç—Ä–∞' in question_lower:
        time_period = "–∑–∞–≤—Ç—Ä–∞"
    elif '–Ω–µ–¥–µ–ª' in question_lower:
        time_period = "–±–ª–∏–∂–∞–π—à—É—é –Ω–µ–¥–µ–ª—é"
    elif '–º–µ—Å—è—Ü' in question_lower:
        time_period = "—Ç–µ–∫—É—â–∏–π –º–µ—Å—è—Ü"
    
    analysis_text = f"""
*–ü—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—ã–π —Ñ–∏–Ω–∞–Ω—Å–æ–≤—ã–π –∞—Å—Ç—Ä–æ–ª–æ–≥–∏—á–µ—Å–∫–∏–π –∞–Ω–∞–ª–∏–∑*

*–í—Ä–µ–º—è:* {time_str}
*–í–æ–ø—Ä–æ—Å:* ¬´{question}¬ª
*–ü–µ—Ä–∏–æ–¥ –∞–Ω–∞–ª–∏–∑–∞:* {time_period}

---

*–ö–∞—Ä—Ç–∞ —Ñ–∏–Ω–∞–Ω—Å–æ–≤:*

‚Ä¢ *–¢—ã (1-–π –¥–æ–º):* –≤ {self._get_rising_sign()} 
‚Ä¢ *–£–ø—Ä–∞–≤–∏—Ç–µ–ª—å ‚Äî {self._get_ruler()}* –≤ {random.randint(1, 30)}¬∞ {self._get_sign()}

‚Ä¢ *–¢–≤–æ–∏ –¥–µ–Ω—å–≥–∏ (2-–π –¥–æ–º):* –≤ {self._get_second_house()} 
‚Ä¢ *–£–ø—Ä–∞–≤–∏—Ç–µ–ª—å ‚Äî {self._get_money_ruler()}* –≤ {random.randint(1, 30)}¬∞ {self._get_sign()}

‚Ä¢ *–ù–µ–æ–∂–∏–¥–∞–Ω–Ω—ã–µ –¥–æ—Ö–æ–¥—ã (8-–π –¥–æ–º):* –≤ {self._get_eighth_house()}
‚Ä¢ *–£–ø—Ä–∞–≤–∏—Ç–µ–ª—å ‚Äî {self._get_eighth_ruler()}* –≤ {random.randint(1, 30)}¬∞ {self._get_sign()}

‚Ä¢ *–û–±—â–∏–µ —Å–∏–≥–Ω–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã –¥–µ–Ω–µ–≥:* 
  ¬∑ –õ—É–Ω–∞ –≤ {moon_sign} - —ç–º–æ—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π –¥–µ–Ω–µ–∂–Ω—ã–π —Ñ–æ–Ω
  ¬∑ –°–æ–ª–Ω—Ü–µ –≤ {sun_sign} - –≤–æ–ª–µ–≤–∞—è —Ñ–∏–Ω–∞–Ω—Å–æ–≤–∞—è —ç–Ω–µ—Ä–≥–∏—è
  ¬∑ –í–µ–Ω–µ—Ä–∞ –≤ {self._get_venus_sign()} - –¥–µ–Ω–µ–∂–Ω–∞—è –≥–∞—Ä–º–æ–Ω–∏—è
  ¬∑ –Æ–ø–∏—Ç–µ—Ä –≤ {self._get_jupiter_sign()} - —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–µ–π

---

*–î–µ—Ç–∞–ª—å–Ω—ã–π —Ñ–∏–Ω–∞–Ω—Å–æ–≤—ã–π –∞–Ω–∞–ª–∏–∑:*
"""
    
    # –ì–õ–£–ë–û–ö–ò–ô –ê–ù–ê–õ–ò–ó –ü–û –¢–ò–ü–£ –í–û–ü–†–û–°–ê
    if '–∑–∞–≤—Ç—Ä–∞' in question_lower or '–ø–æ—Ç–æ–∫' in question_lower:
        analysis_text += self._analyze_tomorrow_finance(moon_sign, sun_sign, time_period)
    elif any(word in question_lower for word in ['–ø—Ä–∏–¥—É—Ç', '–ø–æ–ª—É—á—É', '–∫–æ–≥–¥–∞']):
        analysis_text += self._analyze_money_timing(moon_sign, sun_sign)
    elif any(word in question_lower for word in ['–¥–æ–ª–≥', '–≤–µ—Ä–Ω—É—Ç']):
        analysis_text += self._analyze_debt_return(moon_sign, sun_sign)
    elif any(word in question_lower for word in ['–∏–Ω–≤–µ—Å—Ç', '–≤–ª–æ–∂–µ–Ω']):
        analysis_text += self._analyze_investment(moon_sign, sun_sign)
    else:
        analysis_text += self._analyze_general_finance(moon_sign, sun_sign)
    
    analysis_text += f"\n---\n*–§–∏–Ω–∞–Ω—Å–æ–≤—ã–π –ø—Ä–æ–≥–Ω–æ–∑ –Ω–∞ {time_period}:*\n\n{self._get_detailed_finance_verdict(moon_sign, sun_sign, question_lower, time_period)}"
    
    verdict = self._get_finance_verdict_symbol(moon_sign, sun_sign)
    strategy = self._get_detailed_finance_strategy(moon_sign, sun_sign, time_period)
    
    return verdict, analysis_text, strategy

def _analyze_tomorrow_finance(self, moon_sign, sun_sign, time_period):
    """–î–µ—Ç–∞–ª—å–Ω—ã–π –∞–Ω–∞–ª–∏–∑ —Ñ–∏–Ω–∞–Ω—Å–æ–≤ –Ω–∞ –∑–∞–≤—Ç—Ä–∞/–±–ª–∏–∂–∞–π—à–∏–µ –¥–Ω–∏"""
    aspects = []
    
    # –ê–Ω–∞–ª–∏–∑ –ø–æ –õ—É–Ω–µ - –∫—Ä–∞—Ç–∫–æ—Å—Ä–æ—á–Ω—ã–µ —Ç–µ–Ω–¥–µ–Ω—Ü–∏–∏
